// Definindo as notas e pesos
let n1 = 7.5;
let n2 = 8.0;
let n3 = 6.5;
let p1 = 2;
let p2 = 3;
let p3 = 1;

// Calculando a média ponderada
let media = (n1 * p1 + n2 * p2 + n3 * p3) / (p1 + p2 + p3);

// Exibindo o resultado da média ponderada no console
console.log("A média ponderada das notas é:", media);
